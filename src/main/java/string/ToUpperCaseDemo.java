package string;

/**
 * toUpperCase();转换为大写
 * toLowerCase();转换为小写
 */
public class ToUpperCaseDemo {
    public static void main(String[] args) {
        String name = "SakaiAmusawa";

        String low = name.toLowerCase();
        System.out.println(low);

        String upper = name.toUpperCase();
        System.out.println(upper);
    }
}
